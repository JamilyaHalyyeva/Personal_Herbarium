import mongoose from 'mongoose';


const userShemas = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true,
    },
    password: {
        type: String,
        required: true,
    }
});
const User = mongoose.model('User', userShemas);
    
export default User;
